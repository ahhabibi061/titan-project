// Sparkline with optional regression overlay
function Sparkline({ values, color = '#ed7a2a', regression = false, height = 50, fill = false }) {
  const min = Math.min(...values);
  const max = Math.max(...values);
  const range = max - min || 1;
  const w = 240;
  const padY = 6;
  const points = values.map((v, i) => {
    const x = (i / (values.length - 1)) * w;
    const y = height - padY - ((v - min) / range) * (height - padY * 2);
    return [x, y];
  });
  const linePath = points.map(([x, y], i) => `${i === 0 ? 'M' : 'L'} ${x.toFixed(1)},${y.toFixed(1)}`).join(' ');
  const fillPath = `${linePath} L ${w},${height} L 0,${height} Z`;

  let regressionPath = '';
  if (regression) {
    const reg = linearRegression(values);
    const yA = reg.intercept;
    const yB = reg.slope * (values.length - 1) + reg.intercept;
    const yAs = height - padY - ((yA - min) / range) * (height - padY * 2);
    const yBs = height - padY - ((yB - min) / range) * (height - padY * 2);
    regressionPath = `M 0,${yAs.toFixed(1)} L ${w},${yBs.toFixed(1)}`;
  }

  return (
    <svg viewBox={`0 0 ${w} ${height}`} className="w-full" preserveAspectRatio="none">
      {fill && <path d={fillPath} fill={color} opacity="0.08" />}
      {regression && (
        <path d={regressionPath} stroke="#ff5a2a" strokeWidth="1" strokeDasharray="4,3" fill="none" opacity="0.7" />
      )}
      <path d={linePath} stroke={color} strokeWidth="1.5" fill="none" vectorEffect="non-scaling-stroke" />
      {points.map(([x, y], i) => (
        <circle
          key={i}
          cx={x} cy={y}
          r={i === points.length - 1 ? 3 : 1.4}
          fill={i === points.length - 1 ? color : color}
          opacity={i === points.length - 1 ? 1 : 0.6}
        />
      ))}
    </svg>
  );
}

