// Macro recommendation card — old vs new with diff
function MacroDiff({ current, recommended }) {
  const rows = [
    { key: 'kcal',    label: 'Calories', unit: 'kcal',  cur: current.kcal,    rec: recommended.kcal,    big: true },
    { key: 'protein', label: 'Protein',  unit: 'g',     cur: current.protein, rec: recommended.protein },
    { key: 'carbs',   label: 'Carbs',    unit: 'g',     cur: current.carbs,   rec: recommended.carbs },
    { key: 'fat',     label: 'Fat',      unit: 'g',     cur: current.fat,     rec: recommended.fat },
  ];

  return (
    <div className="space-y-2">
      {rows.map(r => {
        const delta = r.rec - r.cur;
        const isPositive = delta > 0;
        const isZero = delta === 0;
        return (
          <div
            key={r.key}
            className={`grid grid-cols-12 items-baseline gap-2 px-4 ${r.big ? 'py-3' : 'py-2'} border ${
              isZero ? 'border-stone-800/60 bg-stone-950/40' : 'border-orange-500/30 bg-orange-500/5'
            }`}
          >
            <div className="col-span-3 text-[10px] uppercase tracking-wider text-stone-500 font-mono">
              {r.label}
            </div>
            <div className={`col-span-3 font-mono tabular-nums text-stone-500 line-through ${r.big ? 'text-base' : 'text-sm'}`}>
              {fmt0(r.cur)}{r.unit === 'g' && 'g'}
            </div>
            <div className="col-span-1 text-stone-700 font-mono text-xs">→</div>
            <div className={`col-span-3 font-anton tabular-nums ${r.big ? 'text-2xl' : 'text-lg'} ${isZero ? 'text-stone-300' : 'text-orange-300'}`}>
              {fmt0(r.rec)}{r.unit === 'g' && 'g'}
            </div>
            <div className={`col-span-2 text-right font-mono text-[11px] tabular-nums ${
              isZero ? 'text-stone-600' : isPositive ? 'text-orange-400' : 'text-red-400'
            }`}>
              {isZero ? '—' : `${isPositive ? '+' : ''}${delta}${r.unit === 'g' ? 'g' : ''}`}
            </div>
          </div>
        );
      })}
    </div>
  );
}

