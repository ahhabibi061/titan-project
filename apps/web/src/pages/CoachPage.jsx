// Linear regression — returns {slope, intercept} where slope is kg/day.
function linearRegression(values) {
  const n = values.length;
  if (n < 2) return { slope: 0, intercept: values[0] || 0 };
  const meanX = (n - 1) / 2;
  const meanY = values.reduce((a, b) => a + b, 0) / n;
  let num = 0, den = 0;
  for (let i = 0; i < n; i++) {
    num += (i - meanX) * (values[i] - meanY);
    den += (i - meanX) ** 2;
  }
  const slope = den === 0 ? 0 : num / den;
  return { slope, intercept: meanY - slope * meanX };
}

const mean = (arr) => arr.reduce((a, b) => a + b, 0) / arr.length;
const fmt0 = (n) => Math.round(n).toLocaleString('en-US');
const fmt1 = (n) => n.toFixed(1);

// THE RULE GATE — deterministic, testable, returns the decision + reasoning trail.
function decideRecommendation(ctx) {
  const { slope } = linearRegression(ctx.weightLog);
  const slopeKgPerWeek = slope * 7;
  const slopePctPerWeek = (slopeKgPerWeek / ctx.startWeight) * 100;
  const regressingMuscles = Object.entries(ctx.volumeChange)
    .filter(([, v]) => v < -5)
    .map(([k]) => k);

  // Trail records every gate the data passed through — used to highlight
  // the active path in the decision tree visualization below.
  const trail = ['root'];
  let decision = { kcalDelta: 0, deload: false, label: 'Hold', kind: 'hold' };

  if (ctx.goal === 'cut') {
    trail.push('goal_cut');
    if (slopePctPerWeek >= -0.3) {
      trail.push('slope_stalled');
      decision = { kcalDelta: -100, deload: false, label: 'Increase deficit', kind: 'cut_deeper' };
    } else if (slopePctPerWeek < -0.7) {
      trail.push('slope_aggressive');
      if (regressingMuscles.length >= 3) {
        trail.push('volume_regressing');
        decision = { kcalDelta: +150, deload: true, label: 'Add calories + deload', kind: 'recover' };
      } else {
        trail.push('volume_holding');
        decision = { kcalDelta: +100, deload: false, label: 'Add calories', kind: 'protect' };
      }
    } else {
      trail.push('slope_on_target');
      decision = { kcalDelta: 0, deload: false, label: 'Hold course', kind: 'hold' };
    }
  }

  return {
    slope,
    slopeKgPerWeek,
    slopePctPerWeek,
    regressingMuscles,
    trail,
    decision,
  };
}

// Macro re-allocation. Adding kcal: 30% protein, 50% carbs, 20% fat.
// Removing kcal: cut 80% from carbs, 20% from fat (preserve protein).
function applyMacroDelta(current, kcalDelta) {
  if (kcalDelta === 0) return { ...current };
  const ratio = kcalDelta > 0 ? { p: 0.30, c: 0.50, f: 0.20 } : { p: 0.0, c: 0.80, f: 0.20 };
  const p = Math.round((kcalDelta * ratio.p) / 4);
  const c = Math.round((kcalDelta * ratio.c) / 4);
  const f = Math.round((kcalDelta * ratio.f) / 9);
  return {
    kcal: current.kcal + kcalDelta,
    protein: current.protein + p,
    carbs: current.carbs + c,
    fat: current.fat + f,
  };
}

